int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			i++;
			j++;
		}
		else
			i++;
	}
	if (i == j)
		return (1);
	else
		return (0);
}
