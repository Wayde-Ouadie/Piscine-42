int	ft_str_is_printable(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (32 <= str[i] && str[i] <= 126)
		{
			i++;
			j++;
		}
		else
			i++;
	}
	if (i == j)
		return (1);
	else
		return (0);
}
